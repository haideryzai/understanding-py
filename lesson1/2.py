# Create a program that takes two numbers as input and prints their sum, difference, product, and quotient.

number1 = input("please enter first number: ")
number2 = input("please enter second number: ")

sum = int(number1) + int(number2)
difference = int(number1) - int(number2)
product = int(number1) * int(number2)
quotient = int(number1) / int(number2)

print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
